
package db_Operaciones;


public class Plantas_Get_Set {
    
    private int id;
    String Nombre_planta;
    String contenidoDeMinerales;
    String Nivel_ph;
    String Humedad_Suelo;
    String Salinidad;
    String Tipo_tierra;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre_planta() {
        return Nombre_planta;
    }

    public void setNombre_planta(String Nombre_planta) {
        this.Nombre_planta = Nombre_planta;
    }

    public String getContenidoDeMinerales() {
        return contenidoDeMinerales;
    }

    public void setContenidoDeMinerales(String contenidoDeMinerales) {
        this.contenidoDeMinerales = contenidoDeMinerales;
    }

    public String getNivel_ph() {
        return Nivel_ph;
    }

    public void setNivel_ph(String Nivel_ph) {
        this.Nivel_ph = Nivel_ph;
    }

    public String getHumedad_Suelo() {
        return Humedad_Suelo;
    }

    public void setHumedad_Suelo(String Humedad_Suelo) {
        this.Humedad_Suelo = Humedad_Suelo;
    }

    public String getSalinidad() {
        return Salinidad;
    }

    public void setSalinidad(String Salinidad) {
        this.Salinidad = Salinidad;
    }

    public String getTipo_tierra() {
        return Tipo_tierra;
    }

    public void setTipo_tierra(String Tipo_tierra) {
        this.Tipo_tierra = Tipo_tierra;
    }

            
}
